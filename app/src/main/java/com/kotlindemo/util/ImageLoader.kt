object ImageLoader {
    fun loadImage(mContext: Context, url: Any?, imageView: ImageView, placeholder: Int) {
        Glide.with(mContext)
            .load(url)
            .placeholder(placeholder)
            .transition(DrawableTransitionOptions().crossFade())
            .into(imageView)
    }
}